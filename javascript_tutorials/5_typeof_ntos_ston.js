
//                    n to s ----------->  n + ""
let age=22;
age=age+"";
console.log(typeof age,age);

//                          or
age=String(age);
console.log(typeof age,age);





//                    s to n ----------->  + "s"
age="22";
age=+age;
console.log(typeof age,age);

